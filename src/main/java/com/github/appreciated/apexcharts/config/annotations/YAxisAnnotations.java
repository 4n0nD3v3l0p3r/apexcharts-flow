package com.github.appreciated.apexcharts.config.annotations;

public class YAxisAnnotations {
    Double y;
    Double y2;
    Double strokeDashArray;
    String fillColor;
    String borderColor;
    Double opacity;
    Double offsetX;
    Double offsetY;
    Double yAxisIndex;
    AnnotationLabel label;

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }

    public Double getY2() {
        return y2;
    }

    public void setY2(Double y2) {
        this.y2 = y2;
    }

    public Double getStrokeDashArray() {
        return strokeDashArray;
    }

    public void setStrokeDashArray(Double strokeDashArray) {
        this.strokeDashArray = strokeDashArray;
    }

    public String getFillColor() {
        return fillColor;
    }

    public void setFillColor(String fillColor) {
        this.fillColor = fillColor;
    }

    public String getBorderColor() {
        return borderColor;
    }

    public void setBorderColor(String borderColor) {
        this.borderColor = borderColor;
    }

    public Double getOpacity() {
        return opacity;
    }

    public void setOpacity(Double opacity) {
        this.opacity = opacity;
    }

    public Double getOffsetX() {
        return offsetX;
    }

    public void setOffsetX(Double offsetX) {
        this.offsetX = offsetX;
    }

    public Double getOffsetY() {
        return offsetY;
    }

    public void setOffsetY(Double offsetY) {
        this.offsetY = offsetY;
    }

    public Double getyAxisIndex() {
        return yAxisIndex;
    }

    public void setyAxisIndex(Double yAxisIndex) {
        this.yAxisIndex = yAxisIndex;
    }

    public AnnotationLabel getLabel() {
        return label;
    }

    public void setLabel(AnnotationLabel label) {
        this.label = label;
    }
}
